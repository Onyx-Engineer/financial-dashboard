// Corporate action types
export type CorporateActionType = "dividend" | "split" | "spinoff" | "merger";

export interface CorporateAction {
  type: CorporateActionType;
  date: string;
  details: string;
  impactOnShares?: number;
  impactOnPrice?: number;
}

